lbsInput = document.getElementById('lbsInput');
lbsInput.addEventListener('input',asd);
function asd(e){
    let lbs = e.target.value;
    let gram = lbs/0.0022046;
    let kg = lbs/2;
    let ounce = lbs*16;
    document.getElementById('KilogramsOutput').innerHTML=kg;
    document.getElementById('gramsOutput').innerHTML=gram;
    document.getElementById('OzOutput').innerHTML=ounce;
}

